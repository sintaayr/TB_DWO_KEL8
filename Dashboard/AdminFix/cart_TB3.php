    $query = "SELECT salesterritory.Name, coalesce(sum(fakta_kotaterlaris.TotalPenjualan),0) as TotalPenjualan
    from salesterritory
    left join fakta_kotaterlaris on salesterritory.TerritoryID=fakta_kotaterlaris.TerritoryId                                                    
    group by salesterritory.Name                                                            
    ORDER BY TotalPenjualan DESC";
    $result = $conn->query($query);

